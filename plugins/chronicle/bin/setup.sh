#!/usr/bin/env bash
# setup.sh — bootstrap chronicle's Daily/ in a new workspace.
#
# Idempotent: safe to re-run. Does the following, skipping steps already done:
#
#   1. Create or clone $HOME/Daily/  (user provides repo URL, OR "init" for fresh)
#   2. Symlink <workspace>/Daily -> $HOME/Daily   (if invoked inside a workspace)
#   3. Add  Daily   to workspace's .gitignore
#   4. Install the 22:00 launchd / cron rollup job
#   5. Print next-step instructions
#
# Usage:
#   setup.sh git@github.com:<user>/Daily.git    # clone existing repo
#   setup.sh https://github.com/<user>/Daily    # clone via https
#   setup.sh init                                # local-only repo, no remote
#   setup.sh                                     # interactive prompt
#
# Workspace detection: run from the workspace root, OR pass --workspace=<path>.

set -euo pipefail

DAILY_ROOT="${DAILY_ROOT:-$HOME/Daily}"
WORKSPACE=""
REPO_ARG="${1:-}"

# Parse --workspace flag if given.
for arg in "$@"; do
    case "$arg" in
        --workspace=*) WORKSPACE="${arg#--workspace=}";;
    esac
done
[ -z "$WORKSPACE" ] && WORKSPACE="$(pwd)"

ok()   { printf "\033[32m✓\033[0m %s\n" "$*"; }
info() { printf "\033[34m→\033[0m %s\n" "$*"; }
warn() { printf "\033[33m⚠\033[0m %s\n" "$*"; }
err()  { printf "\033[31m✗\033[0m %s\n" "$*" >&2; }

# ── 1. Create / clone $HOME/Daily ─────────────────────────────────────────

if [ -d "$DAILY_ROOT/.git" ]; then
    ok "$DAILY_ROOT already exists as a git repo — skipping clone"
else
    if [ -z "$REPO_ARG" ] || [ "$REPO_ARG" = "--workspace="* ]; then
        echo "Provide your Daily repo. Options:"
        echo "  1) git@github.com:<user>/Daily.git    (existing private repo)"
        echo "  2) init                                (local-only, no remote)"
        printf "→ enter repo URL or 'init': "
        read -r REPO_ARG
    fi

    if [ "$REPO_ARG" = "init" ]; then
        info "creating empty $DAILY_ROOT (local-only mode)"
        mkdir -p "$DAILY_ROOT"
        ( cd "$DAILY_ROOT" && git init -b main >/dev/null )
        ok "initialized $DAILY_ROOT (no remote — push will be skipped)"
    else
        info "cloning $REPO_ARG → $DAILY_ROOT"
        if ! git clone "$REPO_ARG" "$DAILY_ROOT"; then
            err "clone failed. Check your URL/SSH key and re-run."
            exit 1
        fi
        ok "cloned $DAILY_ROOT"
    fi
fi

# Ensure README + .gitignore exist in the Daily repo (only on first init).
if [ ! -f "$DAILY_ROOT/README.md" ]; then
    cat > "$DAILY_ROOT/README.md" <<'EOF'
# Daily

Per-day session log + Excalidraw rollup, generated by the chronicle plugin.

```
Daily/
└── YYYY-MM-DD/
    ├── YYMMDD-HHMM-<slug>.txt   one per session, written when it ends
    ├── canvas-YYMMDD.excalidraw rollup, built once at ~22:00
    └── .closed                   sentinel: day is finalized
```

After `.closed` exists, new sessions automatically route to tomorrow's folder.
EOF
    ok "wrote $DAILY_ROOT/README.md"
fi
if [ ! -f "$DAILY_ROOT/.gitignore" ]; then
    cat > "$DAILY_ROOT/.gitignore" <<'EOF'
_pngs/
.DS_Store
*.swp
EOF
    ok "wrote $DAILY_ROOT/.gitignore"
fi

# ── 2. Symlink workspace/Daily -> $HOME/Daily ─────────────────────────────

if [ -d "$WORKSPACE/.git" ] || [ -f "$WORKSPACE/.git" ]; then
    LINK="$WORKSPACE/Daily"
    if [ -L "$LINK" ]; then
        ok "$LINK already symlinked"
    elif [ -e "$LINK" ]; then
        warn "$LINK exists and is NOT a symlink — leaving alone (move it manually)"
    else
        ln -s "$DAILY_ROOT" "$LINK"
        ok "symlinked $LINK -> $DAILY_ROOT"
    fi

    # ── 3. Add Daily to workspace .gitignore ──────────────────────────────
    GITIGNORE="$WORKSPACE/.gitignore"
    touch "$GITIGNORE"
    if ! grep -qxF "Daily" "$GITIGNORE" && ! grep -qxF "/Daily" "$GITIGNORE"; then
        echo "" >> "$GITIGNORE"
        echo "# chronicle: per-user Daily log lives in \$HOME/Daily/" >> "$GITIGNORE"
        echo "Daily" >> "$GITIGNORE"
        ok "added 'Daily' to $GITIGNORE"
    else
        ok "$GITIGNORE already ignores Daily"
    fi
else
    info "no workspace git repo at $WORKSPACE — skipping symlink + gitignore"
fi

# ── 4. Install rollup cron ────────────────────────────────────────────────

INSTALL_CRON="$(cd "$(dirname "$0")/.." && pwd)/skills/chronicle-daily/bin/install-cron.sh"
if [ -x "$INSTALL_CRON" ]; then
    if "$INSTALL_CRON"; then
        ok "rollup job installed (fires daily at 22:00)"
    else
        warn "rollup job install failed — re-run $INSTALL_CRON manually"
    fi
else
    warn "couldn't find install-cron.sh at $INSTALL_CRON"
fi

# ── 5. Smoke + next steps ─────────────────────────────────────────────────

echo ""
ok "setup complete."
echo ""
echo "Next:"
echo "  • Try logging a test session:"
echo "      echo 'test' | $(dirname "$0")/../skills/chronicle-session/bin/write-session.sh setup-test"
echo "  • Or just say to Claude: \"log this session as <slug>\""
echo ""
echo "Files:"
echo "  📂 $DAILY_ROOT/                  (your private Daily repo)"
[ -L "$WORKSPACE/Daily" ] && echo "  🔗 $WORKSPACE/Daily               (symlink)"
echo "  ⏰ launchd job dev.${USER}.chronicle-daily   (22:00 daily)"
